# Python é uma linguagem de programação bem famosa tanto no mercado quando em linguagens para se aprender.
# Algumas características do Python são:
    # * Propósito geral: esta linguagem possui uma aplicação bem ampla. Ao contrário de linguagens como
        # PHP (focada em desenvolvimento web) e Java (focado em desenvolvimento local/Android), o Python não
        # é uma linguagem voltada para uma área somente.
    # * Fácil e intuitivo: por sua natureza de alto nível, o Python é uma linguagem relativamente fácil de
        # se entender e intuitiva para se codificar, com comandos simples.
    # * Multiplataforma: a linguagem Python é suportada em diversas plataformas, não dependendo de
        # bibliotecas específicas de certos sistemas operacionais. O mesmo código que você escreveu em
        # Windows rodará eum um sistema Linux, e o contrário também é verdade.
    # * Autossuficiente: o Python não precisa de receber conteúdos adicionais instalados, como bibliotecas
        # específicas. A linguagem já possui uma extensa gama de funções variadas que não necessitam de
        # receber instalações adicionais.
    # * Livre: assim como alguns sistemas operacionais, a linguagem Python tem o seu código aberto para
        # estudo, alteração e redistribuição.
    # * Organizada: as áreas de diferentes funções do Python são destacadas, o que significa que a
        # linguagem é bem organizada.
    # * Orientada a objetos: assim como a linguagem C++, o Python é uma linguagem orientada a objetos.
        # Uma linguagem orientada a objetos manuseia as estruturas de um programa baseada em classes e
        # objetos.