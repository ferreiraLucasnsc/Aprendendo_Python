# Crie um script que leia dois números e mostre a soma destes números

numero1 = input('Digite o primeiro número da soma: ')
numero2 = input('Digite o segundo número da soma: ')
print('A soma entre', int(numero1), 'e', int(numero2), 'é igual a', int(numero1) + int(numero2))

# Note: os valores digitados para as variáveis são STRINGS (textos). Como não é possível fazer algum
# tipo de operação matemática com textos, devemos converter estes valores para números, neste caso,
# inteiros (int).